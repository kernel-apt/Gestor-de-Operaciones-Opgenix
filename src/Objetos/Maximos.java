public class Maximos {
        private int maximoEjecucion;
        private int maximoCreacion;

        public Maximos(int maximoEjecucion, int maximoCreacion) {
            this.maximoEjecucion = maximoEjecucion;
            this.maximoCreacion = maximoCreacion;
        }

        public int getMaximoEjecucion() {
            return maximoEjecucion;
        }

        public int getMaximoCreacion() {
            return maximoCreacion;
        }
}
